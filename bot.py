async def ask_question(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()

    data = query.data

    # Начинаем тест
    if data == "q_0":
        if "answers" in context.user_data:
            context.user_data["answers"] = []
        q_num = 0
    # Обрабатываем ответ
    elif data.startswith("a_"):
        _, q_num, answer = data.split("_")
        q_num = int(q_num)
        answer = int(answer)

        if "answers" not in context.user_data:
            context.user_data["answers"] = []

        # Защита от повторного ответа на тот же вопрос
        if len(context.user_data["answers"]) > q_num:
            await query.answer("Вы уже ответили на этот вопрос.", show_alert=True)
            return

        context.user_data["answers"].append(answer)
        q_num += 1

        # Проверяем, все ли вопросы пройдены
        if q_num >= len(QUESTIONS):
            return await show_result(update, context)
